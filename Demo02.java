/*
常量

概念：程序运行期间，固定不变

分类：
1. 字符串。用英文双引号。比如"aB发的"
2. 整数常量。比如100284
3，浮点数(小数)。例如3.14，-2.28
4. 字符常量。用英文单引号'a'.
5. 布尔常量。只有两种取值true, false
 */
public class Demo02 {
    public static void main(String[] args) {
        //字符串常量
        System.out.println("徐上");
        System.out.println("色如果法人身份果然是的故事如果是如果是埃尔官府的人还记得他以后sergeantdtyjftyhft 4356575");
        System.out.println(" ");
        System.out.println("上面是啥都没写内容");

        //整数常量
        System.out.println(123);
        System.out.println(-3253);
//        System.out.println(1000000000000); //整数太大

        //自己回家去试浮点常量，字符常量

        //布尔
        System.out.println(true);
        System.out.println(false);
    }
}
